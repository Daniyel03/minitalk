#include "ft_printf.h"

long	ft_numlen( long res)
{
	int	len;

	len = 1;
	if (res < 0)
	{
		res = (res * -1);
		len++;
	}
	while (res >= 10)
	{
		res /= 10;
		len++;
	}
	return (len);
}
